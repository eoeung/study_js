// 증가 연산자, 감소 연산자
let num2 = 10;
let result = num2++; // 증가시키기 전의 값을 result에 담는다.
console.log(result);
console.log('----------------------------');

let num3 = 30;
let result3 = ++num3; // 증가시킨 값을 result2에 넣음
console.log(result3);
console.log('----------------------------');